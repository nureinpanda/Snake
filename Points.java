
public class Points {
	public int xpos;
	public int ypos;
	
	public int getXpos() {
		return xpos;
	}
	public void setXpos(int xpos) {
		this.xpos = xpos;
	}
	public int getYpos() {
		return ypos;
	}
	public void setYpos(int ypos) {
		this.ypos = ypos;
	}
	
	public Points(int xpos, int ypos) {
		this.xpos=xpos;
		this.ypos=ypos;
	}
	public int getXposbefore() {
		return xpos;
	}
	public int getYposbefore() {
		return ypos;
	}
	
}
